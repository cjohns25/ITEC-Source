package week01;

public class CToF 
{
  public static void main( String[] args)
  {
    // take celcius multiply by 9
    // divide by 5
    // then add 32
    // (9*c)/5 + 32
    
    double celcius = 100;
    double fahrenheit = (9*celcius)/5 + 32;
    
    System.out.println(fahrenheit);
    // print out fahrenheit
    
  }  
  
}